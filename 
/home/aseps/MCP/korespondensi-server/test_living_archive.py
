
from playwright.sync_api import sync_playwright
import time

def test_living_archive():
    with sync_playwright() as p:
        browser = p.chromium.launch(headless=False)
        page = browser.new_page()
        
        # Open FastAPI dashboard
        page.goto("http://localhost:8082/")
        print("✅ Opened http://localhost:8082/")
        
        # Wait for sidebar
        page.wait_for_selector('.sidebar')
        
        # Screenshot before
        page.screenshot(path="before_click.png")
        
        # Find and click Living Archive
        living_link = page.locator('#living-archive-link')
        assert living_link.is_visible()
        href = living_link.get_attribute('href')
        print(f"🔗 Living Archive href: {href}")
        
        # Click
        living_link.click()
        
        # Wait new tab or navigation
        time.sleep(2)
        
        # Screenshot after
        page.screenshot(path="after_click.png")
        
        print("✅ Test complete - Check screenshots")
        browser.close()

if __name__ == "__main__":
    test_living_archive()

